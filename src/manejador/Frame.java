    public void hilo(){
        this.anima = new Thread(new Runnable() {
            @Override
            public void run() {
                try{
                leerIma();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"La cadena no es v√°lida","Error",0);
                    System.out.println("Error: "+e);
                }
            }
        });
        this.anima.start();
    }
    
    public void leerIma(){
        Imagen b = new Imagen();
        if(this.TextCadena.getText().equals("")){
            JOptionPane.showMessageDialog(null,"La caja de texto esta vacia","Error",0);   
        }
        else{
            b.leerImagen(this.LblImagen,this.TextCadena.getText());
        }
    }

